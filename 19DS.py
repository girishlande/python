d={1:"Girish",2:"Ajit",3:"Suhas"}
print(d)

print()

#accessing keys in dictionary
for i in d.keys():
    print(f"{i} = > {d[i]}")
    
print()
    
#accessing items in dictionary
for i,v in d.items():
    print(f"{i} = > {v}")

print()

#accessing values in dictionary
for x in d.values(): 
    print(x)
print()    
    